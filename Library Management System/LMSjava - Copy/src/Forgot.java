    public void Search(){
        String a1 = jTextField1.getText();
        String sql = "Select * from account where Username ='"+a1+"'";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
        if(rs.next()){
            jTextField2.setText(rs.getString(2));
            jTextField3.setText(rs.getString(4));
            rs.close();
            pst.close();
            
        }else{
            jTextField1.setBorder(red);
            JOptionPane.showMessageDialog(null,"Incorrect username");
        }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    public void Retrive(){
        
        String a1 = jTextField1.getText();
        String a2 = jTextField4.getText();
        String sql = "Select * from account where Answer='"+a2+"' AND Username='"+a1+"'";
        try{
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            if(rs.next()){
                jTextField5.setText(rs.getString(3));
            }else{
                JOptionPane.showMessageDialog(null,"Your Answer is incorrect , Please rethink");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }
  
